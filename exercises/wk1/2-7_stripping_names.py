'''
Stripping Names

This program prints out names that have been stripped of whitespace.

Original Author: Joseph Kaiser
Creation Date: 10/31/2025
'''

name = '\t john pork\n     \n   \t'
# print(name)
name_left_strip = name.lstrip()
name_right_strip = name.rstrip()
name_strip = name.strip()
print(name_left_strip)
print(name_right_strip)
print(name_strip)
print(name_strip)
